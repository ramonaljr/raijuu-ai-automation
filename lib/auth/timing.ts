/**
 * Constant-time string comparison. Use for any bearer-token / secret check so
 * timing doesn't leak a character-by-character prefix match to an attacker.
 */
export function timingSafeEqual(a: string, b: string): boolean {
  if (a.length !== b.length) return false;
  let mismatch = 0;
  for (let i = 0; i < a.length; i++) {
    mismatch |= a.charCodeAt(i) ^ b.charCodeAt(i);
  }
  return mismatch === 0;
}
